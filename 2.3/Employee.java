class Employee {

	private int id;
	private String name;
	private String dept;
	private int sal;
	
	void setId(int id) {
		this.id=id;
	}
	
	void setName(String name) {
		this.name=name;
	}
	
	void setDept(String dept) {
		this.dept=dept;
	}
	
	void setSal(int sal) {
		this.sal=sal;
	}
	
	int getId() {
		return id;
	}
	
	String getName() {
		return name;
	}
	
	String getDept() {
		return dept;
	}
	
	int getSal() {
		return sal;
	}

}
